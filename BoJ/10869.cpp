#include <bits/stdc++.h>
using namespace std;

int main()
{
    int numA, numB;
    cin >> numA >> numB;
    cout << (int)(numA+numB) << "\n";
    cout << (int)(numA-numB) << "\n";
    cout << (int)(numA*numB) << "\n";
    cout << (int)(numA/numB) << "\n";
    cout << (int)(numA%numB) << "\n";
    return 0;
}